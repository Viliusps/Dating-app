CREATE TABLE couples
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    date        TIMESTAMPTZ,
    weight_diff FLOAT,
    first       BIGINT,
    second      BIGINT,
    status      couple_status,
    chat        BIGINT,
    CONSTRAINT couple_pk      PRIMARY KEY (id),
    FOREIGN KEY(first) 
      REFERENCES users(id),
    FOREIGN KEY(second) 
      REFERENCES users(id),
    FOREIGN KEY(chat) 
      REFERENCES chats(id)
);

