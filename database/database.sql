DROP TABLE IF EXISTS hobbies CASCADE;
DROP TABLE IF EXISTS chats CASCADE;
DROP TABLE IF EXISTS messages CASCADE;
DROP TABLE IF EXISTS couples CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TYPE IF EXISTS couple_status CASCADE;
DROP TYPE IF EXISTS roles CASCADE;
DROP TYPE IF EXISTS genders CASCADE;
DROP TYPE IF EXISTS search_genders CASCADE;
DROP TYPE IF EXISTS purposes CASCADE;

CREATE TYPE couple_status AS ENUM 
('LIKES', 'DISLIKES', 'UNDECIDED');

CREATE TYPE roles AS ENUM
('ADMIN', 'USER');

CREATE TYPE genders AS ENUM 
('MALE', 'FEMALE', 'OTHER');

CREATE TYPE search_genders AS ENUM
('MALE', 'FEMALE', 'OTHER', 'ANY');

CREATE TYPE purposes AS ENUM
('SHORT', 'LONG');


